#include <stdio.h>

int main()
{
    int a = 30;
    int b = 7;

    printf("Addition : %d\n", a + b);
    printf("Substraction : %d\n", a - b);
    printf("Multipication : %d\n", a * b);
    printf("Division : %d\n", a / b);
    printf("Remainder : %d\n", a % b); // % return ভাগশেষ

    return 0;
}

