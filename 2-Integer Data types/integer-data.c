#include <stdio.h>

int main()
{
    // Signed Integer data

    // short - %d, %hi 
    short a = 45;
    printf("I am short %hi\n", a);

    // int - %d, %i 
    int b = 1000;
    printf("I am int %d\n", b);


    // long - %dl, %il 
    long c = 1200L;
    printf("I am long %dl\n", c);


    return 0;
}